/** Upsert a company by name (case-insensitive). Returns the company row. */
export async function upsertCompany(input: {
  name: string;
  country?: string | null;
  contact_email?: string | null;
  contact_phone?: string | null;
}): Promise<Company> {
  const sb = createServiceSupabase();
  const trimmed = input.name.trim();

  // Look up case-insensitively
  const { data: existing } = await sb
    .from('companies')
    .select('*')
    .ilike('name', trimmed)
    .limit(1)
    .maybeSingle();

  if (existing) return existing as Company;

  const { data, error } = await sb
    .from('companies')
    .insert({
      name: trimmed,
      country: input.country ?? null,
      contact_email: input.contact_email ?? null,
      contact_phone: input.contact_phone ?? null
    })
    .select()
    .single();

  if (error) throw new Error(`upsertCompany: ${error.message}`);
  return data as Company;
}

/**
 * Upsert a vessel by IMO number (C8). If imo_no is missing, falls back to a
 * fuzzy name+company match; if neither found, inserts a stub row.
 */
export async function upsertVessel(input: {
  name: string;
  imo_no?: string | null;
  company_id?: string | null;
  vessel_type?: string | null;
  flag?: string | null;
}): Promise<Vessel> {
  const sb = createServiceSupabase();
  const trimmedName = input.name.trim();
  const trimmedImo = input.imo_no?.toString().trim() || null;

  if (trimmedImo) {
    const { data: existingByImo } = await sb
      .from('vessels')
      .select('*')
      .eq('imo_no', trimmedImo)
      .maybeSingle();
    if (existingByImo) return existingByImo as Vessel;
  }

  if (input.company_id) {
    const { data: existingByName } = await sb
      .from('vessels')
      .select('*')
      .eq('company_id', input.company_id)
      .ilike('name', trimmedName)
      .limit(1)
      .maybeSingle();
    if (existingByName) return existingByName as Vessel;
  }

  const { data, error } = await sb
    .from('vessels')
    .insert({
      name: trimmedName,
      imo_no: trimmedImo,
      company_id: input.company_id ?? null,
      vessel_type: input.vessel_type ?? null,
      flag: input.flag ?? null
    })
    .select()
    .single();
  if (error) throw new Error(`upsertVessel: ${error.message}`);
  return data as Vessel;
}

/** Create a new lead. Also writes a stage_change event for the initial state. */
export async function createLead(input: {
  source: LeadSource;
  track: LeadTrack;
  company_id?: string | null;
  vessel_id?: string | null;
  priority_score?: number;
  priority_reason?: Record<string, unknown>;
  draft_message?: string | null;
  context?: Record<string, unknown>;
}): Promise<Lead> {
  const sb = createServiceSupabase();
  const { data, error } = await sb
    .from('leads')
    .insert({
      source: input.source,
      track: input.track,
      company_id: input.company_id ?? null,
      vessel_id: input.vessel_id ?? null,
      priority_score: input.priority_score ?? 0,
      priority_reason: input.priority_reason ?? {},
      draft_message: input.draft_message ?? null,
      context: input.context ?? {}
    })
    .select()
    .single();
  if (error) throw new Error(`createLead: ${error.message}`);
  const lead = data as Lead;
  await recordEvent(lead.id, 'created', { source: input.source, track: input.track });
  return lead;
}

export async function updateLeadStage(
  id: string,
  stage: LeadStage,
  actor?: string
): Promise<Lead | null> {
  const sb = createServiceSupabase();
  const { data: before } = await sb.from('leads').select('stage').eq('id', id).single();
  const { data, error } = await sb
    .from('leads')
    .update({ stage })
    .eq('id', id)
    .select()
    .single();
  if (error) return null;
  await recordEvent(id, 'stage_change', {
    from: before?.stage ?? null,
    to: stage,
    actor: actor ?? null
  });
  return data as Lead;
}

