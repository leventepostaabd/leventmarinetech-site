export async function recordInboundMessage(input: {
  lead_id?: string | null;
  channel: InboundChannel;
  subject?: string | null;
  body?: string | null;
  attachments?: unknown[];
}): Promise<InboundMessage> {
  const sb = createServiceSupabase();
  const { data, error } = await sb
    .from('inbound_messages')
    .insert({
      lead_id: input.lead_id ?? null,
      channel: input.channel,
      subject: input.subject ?? null,
      body: input.body ?? null,
      attachments: input.attachments ?? []
    })
    .select()
    .single();
  if (error) throw new Error(`recordInboundMessage: ${error.message}`);
  if (input.lead_id) {
    await recordEvent(input.lead_id, 'rfq_received', { channel: input.channel });
  }
  return data as InboundMessage;
}

