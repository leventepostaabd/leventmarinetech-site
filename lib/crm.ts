export async function listLeads(opts: {
  track?: LeadTrack;
  stage?: LeadStage | LeadStage[];
  source?: LeadSource;
  search?: string;
  limit?: number;
} = {}): Promise<LeadWithRefs[]> {
  const sb = createServiceSupabase();
  let q = sb
    .from('leads')
    .select('*, company:companies(*), vessel:vessels(*)')
    .order('priority_score', { ascending: false })
    .order('created_at', { ascending: false })
    .limit(opts.limit ?? 200);

  if (opts.track) q = q.eq('track', opts.track);
  if (opts.source) q = q.eq('source', opts.source);
  if (opts.stage) {
    if (Array.isArray(opts.stage)) q = q.in('stage', opts.stage);
    else q = q.eq('stage', opts.stage);
  }

  const { data, error } = await q;
  if (error) {
    console.error('[crm] listLeads error', error);
    return [];
  }
  const rows = (data ?? []) as unknown as LeadWithRefs[];

  // Client-side filter for search (Postgres full-text is over-kill at this
  // size; a few hundred rows in memory are fine until Phase 2 volume hits).
  if (opts.search && opts.search.trim()) {
    const needle = opts.search.toLowerCase().trim();
    return rows.filter((l) => {
      const haystack = [
        l.company?.name,
        l.vessel?.name,
        l.vessel?.imo_no,
        l.draft_message,
        JSON.stringify(l.context ?? {})
      ]
        .filter(Boolean)
        .join(' ')
        .toLowerCase();
      return haystack.includes(needle);
    });
  }

  return rows;
}

