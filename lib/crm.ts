/**
 * Ingest an inbound form submission into the CRM.
 * - Upserts the company (by name) and vessel (by IMO)
 * - Creates a new lead in stage='new'
 * - Records an inbound_messages row with the raw payload as the body
 *
 * Failures are swallowed and logged — the main RFQ/service request flow
 * must not break if the CRM write fails (the request still lands in
 * rfq_requests / service_requests, which is the canonical record).
 */
export async function ingestInboundForm(input: {
  source: 'service_wizard' | 'supply_rfq';
  track: LeadTrack;
  contact_name?: string | null;
  contact_email?: string | null;
  contact_phone?: string | null;
  company_name?: string | null;
  vessel_name?: string | null;
  imo?: string | null;
  port?: string | null;
  urgency?: string | null;
  system?: string | null;
  brand?: string | null;
  part_number?: string | null;
  description?: string | null;
  raw_payload?: Record<string, unknown>;
}): Promise<{ lead_id: string | null }> {
  try {
    let company: Company | null = null;
    if (input.company_name?.trim()) {
      company = await upsertCompany({
        name: input.company_name,
        contact_email: input.contact_email ?? null,
        contact_phone: input.contact_phone ?? null
      });
    } else if (input.contact_email) {
      // Fallback: use email domain as proxy company
      const proxy = (input.contact_email.split('@')[1] ?? input.contact_email).split('.')[0];
      if (proxy) {
        company = await upsertCompany({
          name: proxy.charAt(0).toUpperCase() + proxy.slice(1),
          contact_email: input.contact_email
        });
      }
    }

    let vessel: Vessel | null = null;
    if (input.vessel_name?.trim() || input.imo?.trim()) {
      vessel = await upsertVessel({
        name: input.vessel_name?.trim() || 'Unknown vessel',
        imo_no: input.imo?.trim() || null,
        company_id: company?.id ?? null
      });
    }

    const context: Record<string, unknown> = {};
    if (input.port) context.port = input.port;
    if (input.urgency) context.urgency = input.urgency;
    if (input.system) context.system = input.system;
    if (input.brand) context.brand = input.brand;
    if (input.part_number) context.part_number = input.part_number;
    if (input.contact_name) context.contact_name = input.contact_name;
    if (input.contact_email) context.contact_email = input.contact_email;
    if (input.contact_phone) context.contact_phone = input.contact_phone;
    if (input.description) context.description = input.description;

    const priority_score = priorityScoreForInbound(input.urgency, input.track);

    const lead = await createLead({
      source: input.source,
      track: input.track,
      company_id: company?.id ?? null,
      vessel_id: vessel?.id ?? null,
      priority_score,
      priority_reason: { reason: 'inbound', urgency: input.urgency ?? null },
      context
    });

    await recordInboundMessage({
      lead_id: lead.id,
      channel: 'form',
      subject: summariseForm(input),
      body: JSON.stringify(input.raw_payload ?? input, null, 2)
    });

    return { lead_id: lead.id };
  } catch (e) {
    console.error('[crm] ingestInboundForm failed', e);
    return { lead_id: null };
  }
}

function summariseForm(input: {
  vessel_name?: string | null;
  imo?: string | null;
  port?: string | null;
  system?: string | null;
  brand?: string | null;
  part_number?: string | null;
  track: LeadTrack;
}): string {
  const bits: string[] = [];
  bits.push(input.track === 'service' ? 'Service request' : 'Supply RFQ');
  if (input.system) bits.push(input.system);
  if (input.brand) bits.push(input.brand);
  if (input.part_number) bits.push(input.part_number);
  if (input.vessel_name) bits.push(`vessel: ${input.vessel_name}`);
  if (input.imo) bits.push(`IMO ${input.imo}`);
  if (input.port) bits.push(`@ ${input.port}`);
  return bits.join(' · ');
}

/** Coarse first-pass scoring for inbound leads. Real model lives in Phase 2. */
function priorityScoreForInbound(urgency: string | null | undefined, track: LeadTrack): number {
  const u = (urgency ?? '').toLowerCase();
  if (u === 'aog' || u === 'now' || u === 'emergency') return 90;
  if (u === '24h' || u === 'urgent') return 70;
  if (track === 'service') return 50;
  return 40;
}

export function stageLabel(stage: LeadStage, locale: Locale = 'en'): string {
  const map = {
    en: { new: 'New', contacted: 'Contacted', replied: 'Replied', quoting: 'Quoting', won: 'Won', lost: 'Lost' },
    tr: { new: 'Yeni', contacted: 'İletildi', replied: 'Yanıtlandı', quoting: 'Teklif', won: 'Kazandı', lost: 'Kayıp' }
  } as const;
  return (map[locale as keyof typeof map] ?? map.en)[stage];
}

