export async function leadCounts(): Promise<{
  total: number;
  byStage: Record<LeadStage, number>;
  byTrack: Record<LeadTrack, number>;
  newThisWeek: number;
}> {
  const sb = createServiceSupabase();
  const since = new Date(Date.now() - 7 * 24 * 3600 * 1000).toISOString();

  const [total, newCount, contacted, replied, quoting, won, lost, svc, sup, newWeek] =
    await Promise.all([
      sb.from('leads').select('id', { count: 'exact', head: true }),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('stage', 'new'),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('stage', 'contacted'),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('stage', 'replied'),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('stage', 'quoting'),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('stage', 'won'),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('stage', 'lost'),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('track', 'service'),
      sb.from('leads').select('id', { count: 'exact', head: true }).eq('track', 'supply'),
      sb
        .from('leads')
        .select('id', { count: 'exact', head: true })
        .gte('created_at', since)
    ]);

  return {
    total: total.count ?? 0,
    byStage: {
      new:       newCount.count ?? 0,
      contacted: contacted.count ?? 0,
      replied:   replied.count ?? 0,
      quoting:   quoting.count ?? 0,
      won:       won.count ?? 0,
      lost:      lost.count ?? 0
    },
    byTrack: {
      service: svc.count ?? 0,
      supply:  sup.count ?? 0
    },
    newThisWeek: newWeek.count ?? 0
  };
}

